            <script>
                // Function to set the active class based on the current page URL
                function setActivePage() {
                    var currentUrl = window.location.href;

                    // Loop through each sidebar item and check if its href matches the current URL
                    var sidebarLinks = document.querySelectorAll('.sidebar-link');
                    sidebarLinks.forEach(function(link) {
                        if (link.href === currentUrl) {
                            // Add the 'active' class to the parent sidebar item
                            link.closest('.sidebar-item').classList.add('active');
                        }
                    });
                }

                // Function to activate the clicked sidebar item
                function activateSidebarItem(element) {
                    // Remove 'active' class from all sibling elements
                    $(element).siblings().removeClass('active');

                    // Add 'active' class to the clicked element
                    $(element).addClass('active');

                    // If the clicked element has a parent with class 'menu-sub', activate the parent too
                    if ($(element).parent().hasClass('sidebar-item')) {
                        activateSidebarItem($(element).parent().parent());

                        // Additionally, add 'open' class to the parent
                        $(element).parent().parent().addClass('open');
                    }
                }

                // Combined function to handle activation and set the active page on load
                function initializeSidebar() {
                    // Remove the 'active' class from all sidebar items
                    var sidebarItems = document.querySelectorAll('.sidebar-item');
                    sidebarItems.forEach(function(item) {
                        item.classList.remove('active');
                    });

                    // Call the setActivePage function when the page loads
                    setActivePage();
                }

                // Call the initializeSidebar function when the page loads
                window.onload = function() {
                    initializeSidebar();
                };
            </script>

